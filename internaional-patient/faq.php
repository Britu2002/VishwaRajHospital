<p class="mb-4 text-justify">
 <span class="font-bold ">What are the common causes of kidney stones?</span> 

Kidney stones can be caused by various factors including recurrent infections like urinary tract infections (UTIs), hot climate leading to increased concentration of solutes in urine, dehydration due to inadequate water intake, metabolic causes such as hyperparathyroidism and gout, immobility or sedentary lifestyle, presence of Randall’s plaques, renal anomalies like PUJ obstruction, family history of stone formation, and certain drugs.
</p>
<p class="mb-4 text-justify">
  <span class="font-bold">Does diet affect the formation of kidney stones?   </span>
  Yes, diet can affect the formation of kidney stones. Not drinking enough water, having a diet rich in red meat, dry fruits, calcium, and oxalates can increase the risk of kidney stone formation.
