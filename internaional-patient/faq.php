<p class="mb-4 text-justify">
 <span class="font-bold">What are some cardiac issues that women should be aware of?</span> 
Heart disease is the most common cardiac issue that women should be aware of. Women are more likely to have heart attack symptoms unrelated to chest pain, such as neck, jaw, shoulder, upper back, or abdominal discomfort, shortness of breath, pain in one or both arms, nausea or vomiting, sweating, light-headedness or dizziness, and unusual fatigue. 
</p>
<p class="mb-4 text-justify">
  <span class="font-bold">What are some risk factors for heart disease in women?</span>
Some risk factors for heart disease in women include diabetes, mental stress and depression, smoking, inactivity, menopause, broken heart syndrome, and pregnancy complications. 
</p>
<p class="mb-4 text-justify">
  <span class="font-bold">What are some risk factors for heart disease in women?  </span>
Women can make several lifestyle changes to reduce the risk of heart disease, including quitting or not starting smoking, exercising regularly, maintaining a healthy weight, and eating a heart-healthy diet that includes whole grains, fruits and vegetables, low-fat or fat-free dairy products, and lean meats. Women should also take prescribed medications appropriately and manage other conditions that are risk factors for heart disease, such as high blood pressure, high cholesterol, and diabetes. 
</p>
